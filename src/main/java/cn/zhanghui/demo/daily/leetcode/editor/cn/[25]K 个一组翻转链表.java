import cn.zhanghui.demo.daily.leetcode.editor.cn.component.ListNode;

import java.util.LinkedList;

/**
 * Definition for singly-linked list.
 * public class ListNode {
 * int val;
 * ListNode next;
 * ListNode() {}
 * ListNode(int val) { this.val = val; }
 * ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution25 {

    public ListNode reverseKGroup(ListNode head, int k) {
        ListNode dummy = new ListNode(0);
        dummy.next = head;

        ListNode pre = dummy;
        ListNode end = dummy;

        while (end.next != null) {
            for (int i = 0; i < k && end != null; i++) end = end.next;
            if (end == null) break;
            ListNode start = pre.next;
            ListNode next = end.next;
            end.next = null;
            // è¿™é‡Œä½¿ç”¨åŸåœ°åè½¬çš„æ­¥éª¤ ä¼˜åŒ–äº†ä½¿ç”¨æ ˆåè½¬çš„è¿‡ç¨‹
            pre.next = reverse(start);
            start.next = next;
            pre = start;

            end = pre;
        }
        return dummy.next;
    }

    private ListNode reverse(ListNode head) {
        ListNode pre = null;
        ListNode curr = head;
        while (curr != null) {
            ListNode next = curr.next;
            curr.next = pre;
            pre = curr;
            curr = next;
        }
        return pre;
    }

    public ListNode reverseKGroup1(ListNode head, int k) {
        if (head == null || head.next == null) return head;

        // è¿™é‡Œç”¨æ ˆçš„ç»“æ„ ä¾¿äºç†è§£ åè½¬çš„è¿‡ç¨‹
        LinkedList<ListNode> stack = new LinkedList<>();

        ListNode next = head;
        ListNode root = null;

        ListNode lastExchange = null;
        while (next != null) {
            ListNode temp = next.next;
            if (stack.size() >= k - 1) {
                ListNode begin = next;
                while (stack.size() > 0) {
                    next.next = stack.poll();
                    next = next.next;
                }
                if (lastExchange != null) {
                    lastExchange.next = begin;
                } else {
                    root = begin;
                }
                lastExchange = next;
                next.next = temp;
            } else {
                stack.addFirst(next);
            }
            next = next.next;
        }
        return root;
    }
}
